def sumoftwo(a, b):
    """
    This is an sumoftwo function to add two number

    :param int a: first number to add
    :param int b: first number to add
    :return: return addition of the integer
    :rtype: int
    :example:
        >>> r = sumoftwo(2, 3)
        >>> r
        5

    """
    return a + b
